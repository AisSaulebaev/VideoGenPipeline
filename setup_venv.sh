#!/bin/bash
# Скрипт для создания виртуального окружения и установки зависимостей (Linux/macOS)

echo "========================================"
echo "Создание виртуального окружения..."
echo "========================================"

# Проверяем наличие Python
if ! command -v python3 &> /dev/null; then
    echo "ОШИБКА: Python3 не найден! Установите Python 3.10+"
    exit 1
fi

# Создаем виртуальное окружение
if [ -d "venv" ]; then
    echo "Виртуальное окружение уже существует. Удаляю старое..."
    rm -rf venv
fi

python3 -m venv venv
if [ $? -ne 0 ]; then
    echo "ОШИБКА: Не удалось создать виртуальное окружение!"
    exit 1
fi

echo ""
echo "========================================"
echo "Активация окружения и установка зависимостей..."
echo "========================================"

source venv/bin/activate

echo "Обновление pip..."
python -m pip install --upgrade pip

echo ""
echo "Установка зависимостей из requirements.txt..."
pip install -r requirements.txt

if [ $? -ne 0 ]; then
    echo ""
    echo "ОШИБКА: Не удалось установить зависимости!"
    exit 1
fi

echo ""
echo "========================================"
echo "Готово! Виртуальное окружение создано."
echo "========================================"
echo ""
echo "Для запуска используйте: ./run.sh"
echo ""

